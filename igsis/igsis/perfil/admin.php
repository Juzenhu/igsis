<p>Uma folha de estilo é dita externa, quando as regras CSS são declaradas em um documento a parte do documento HTML, isto é, a folha de estilo é um arquivo separado do arquivo html. Um arquivo de folha de estilo deve ser gravado com a extensão .css</p>
<p>Uma folha de estilo externa é ideal para ser aplicada a várias páginas. Com uma folha de estilo externa , você pode mudar a aparência de um site inteiro mudando regras de estilos contidas em um arquivo apenas (o arquivo da folha de estilo).</p>
<p>O arquivo css da folha de estilo externa deverá ser lincado ou importado ao documento HTML, dentro da seçãohead do documento. A sintaxe geral para lincar uma folha de estilo chamada estilo.css é mostrada a seguir.</p>
